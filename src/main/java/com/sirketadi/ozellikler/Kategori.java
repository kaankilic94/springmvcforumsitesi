package com.sirketadi.ozellikler;

public class Kategori {
	private String katid;
	private String katadi;
	private String kataciklama;
	
	public String getKatid() {
		return katid;
	}
	public void setKatid(String katid) {
		this.katid = katid;
	}
	public String getKatadi() {
		return katadi;
	}
	public void setKatadi(String katadi) {
		this.katadi = katadi;
	}
	public String getKataciklama() {
		return kataciklama;
	}
	public void setKataciklama(String kataciklama) {
		this.kataciklama = kataciklama;
	}
	
	
}
